const { Schema } = requrie('mongoose')
const dateFormat = require('../utils/dateFormat')

const replySchema = new Schema (
    {
        replyBody: {
            type: String,
            required: true,
            minlength: 1,
            maxlength: 280
        },
        username: {
            type: String,
            required: true
        },
        createdAt: {
            type: Date,
            default: Date.now,
            get: timestamp => dateFormat(timestamp)
        }
    },
    {
        toJSON: {
            getters: true
        }
    }
)

module.exports = replySchema